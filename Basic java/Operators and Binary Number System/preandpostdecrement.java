public class preandpostdecrement {
    public static void main(String[] args) {
        // Post decrement
        int a=10;
        int b=0;
        b=a--;
        System.out.println(a);
        System.out.println(b);
        // Pre decrement
        int c=10;
        int d=0;
        d=--c;
        System.out.println(c);
        System.out.println(d);
    }
}
